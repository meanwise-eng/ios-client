import Foundation
import UIKit

// MARK: View

extension UIView {
    
    // Get height
    func getHeight() -> CGFloat {
        return frame.size.height
    }
    
    // Get height
    func getWidth() -> CGFloat {
        return frame.size.width
    }
    
    // Get height
    func getX() -> CGFloat {
        return frame.origin.x
    }
    
    // Get height
    func getY() -> CGFloat {
        return frame.origin.y
    }
    
}